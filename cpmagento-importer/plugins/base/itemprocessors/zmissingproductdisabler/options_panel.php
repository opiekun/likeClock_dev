<div class="plugin_description">

This plugin automatically disables existing products if they are not existing in your data source. Disabling in this case means: qty is set to zero, is_in_stock is set to false and status is set to disabled.
<br/>- If you want to keep stock etc. intact the plugins zmissingproductdisabler.php file, at the top of the file is a configuration section.
<br/>- E-mail notifications are sent, you can specify the mailadres in the plugins file zmissingproductdeleter.php at the top of the file.

